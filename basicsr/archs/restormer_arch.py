from basicsr.utils.registry import ARCH_REGISTRY
@ARCH_REGISTRY.register()
class Restormer(nn.Module):
    def __init__(self, 
        inp_channels=3, 
        out_channels=6, 
        dim = 48,
        num_blocks = [4,6,6,8], 
        num_refinement_blocks = 4,
        heads = [1,2,4,8],
        ffn_expansion_factor = 2.66,
        bias = False,
        kernel_size = 21,
        LayerNorm_type = 'WithBias',   ## Other option 'BiasFree'
        dual_pixel_task = False,        ## True for dual-pixel defocus deblurring only. Also set inp_channels=6
        scale = 2
    ):

        super(Restormer, self).__init__()


        self.t_embedder = nn.Sequential(
                PositionalEncoding(dim),
                nn.Linear(dim, dim * 4),
                nn.Mish(),
                nn.Linear(dim * 4, dim)
            )
            
        self.scale = scale 
        
        self.patch_embed = OverlapPatchEmbed(2 * inp_channels, dim)

        self.predictor=nn.Sequential(
            nn.Conv2d(inp_channels, dim, 3, 3, 1),
            nn.AdaptiveAvgPool2d(1),
        )
        
        self.encoder_level1 = nn.Sequential(*[TransformerBlock(dim=dim, num_heads=heads[0], ffn_expansion_factor=ffn_expansion_factor, bias=bias, LayerNorm_type=LayerNorm_type, embed_dim=dim) for i in range(num_blocks[0])])
        
        self.down1_2 = Downsample(dim) ## From Level 1 to Level 2
        self.encoder_level2 = nn.Sequential(*[TransformerBlock(dim=int(dim*2**1), num_heads=heads[1], ffn_expansion_factor=ffn_expansion_factor, bias=bias, LayerNorm_type=LayerNorm_type, embed_dim=dim) for i in range(num_blocks[1])])
        
        self.down2_3 = Downsample(int(dim*2**1)) ## From Level 2 to Level 3
        self.encoder_level3 = nn.Sequential(*[TransformerBlock(dim=int(dim*2**2), num_heads=heads[2], ffn_expansion_factor=ffn_expansion_factor, bias=bias, LayerNorm_type=LayerNorm_type, embed_dim=dim) for i in range(num_blocks[2])])
        
        self.down3_4 = Downsample(int(dim*2**2)) ## From Level 3 to Level 4
        self.latent = nn.Sequential(*[TransformerBlock(dim=int(dim*2**3), num_heads=heads[3], ffn_expansion_factor=ffn_expansion_factor, bias=bias, LayerNorm_type=LayerNorm_type, embed_dim=dim) for i in range(num_blocks[3])])
        
        self.up4_3 = Upsample(int(dim*2**3)) ## From Level 4 to Level 3
        self.reduce_chan_level3 = nn.Conv2d(int(dim*2**3), int(dim*2**2), kernel_size=1, bias=bias)
        self.decoder_level3 = nn.Sequential(*[TransformerBlock(dim=int(dim*2**2), num_heads=heads[2], ffn_expansion_factor=ffn_expansion_factor, bias=bias, LayerNorm_type=LayerNorm_type, embed_dim=dim) for i in range(num_blocks[2])])

        self.up3_2 = Upsample(int(dim*2**2)) ## From Level 3 to Level 2
        self.reduce_chan_level2 = nn.Conv2d(int(dim*2**2), int(dim*2**1), kernel_size=1, bias=bias)
        self.decoder_level2 = nn.Sequential(*[TransformerBlock(dim=int(dim*2**1), num_heads=heads[1], ffn_expansion_factor=ffn_expansion_factor, bias=bias, LayerNorm_type=LayerNorm_type, embed_dim=dim) for i in range(num_blocks[1])])
        
        self.up2_1 = Upsample(int(dim*2**1))  ## From Level 2 to Level 1  (NO 1x1 conv to reduce channels)

        self.decoder_level1 = nn.Sequential(*[TransformerBlock(dim=int(dim*2**1), num_heads=heads[0], ffn_expansion_factor=ffn_expansion_factor, bias=bias, LayerNorm_type=LayerNorm_type, embed_dim=dim) for i in range(num_blocks[0])])
        
        self.refinement = nn.Sequential(*[TransformerBlock(dim=int(dim*2**1), num_heads=heads[0], ffn_expansion_factor=ffn_expansion_factor, bias=bias, LayerNorm_type=LayerNorm_type, embed_dim=dim) for i in range(num_refinement_blocks)])
        
        self.k_mlp = nn.Sequential(
            nn.Linear(dim, kernel_size ** 2),
            nn.Mish(),
            nn.Linear(kernel_size ** 2, kernel_size ** 2),
            nn.Softmax(1)
            )
        #### For Dual-Pixel Defocus Deblurring Task ####
        self.dual_pixel_task = dual_pixel_task
        if self.dual_pixel_task:
            self.skip_conv = nn.Conv2d(dim, int(dim*2**1), kernel_size=1, bias=bias)
        ###########################
            
        self.output = nn.Conv2d(int(dim*2**1), out_channels, kernel_size=3, stride=1, padding=1, bias=bias)

    def forward(self, inp_img, t, cond):
        t_embed = self.t_embedder(t)
        
        cond_up = F.interpolate(cond, scale_factor=self.scale, mode='bicubic')
        
        k = self.predictor(cond).squeeze().squeeze()
        res_k = k
        
        inp_enc_level1 = self.patch_embed(torch.cat([inp_img,cond_up],dim=1))
        
        for encoder1_block in self.encoder_level1:
            inp_enc_level1, _, k = encoder1_block(inp_enc_level1, t_embed, k)
        out_enc_level1 = inp_enc_level1
        
        inp_enc_level2 = self.down1_2(out_enc_level1)
        for encoder2_block in self.encoder_level2:
            inp_enc_level2, _, k = encoder2_block(inp_enc_level2, t_embed, k)
        out_enc_level2 = inp_enc_level2

        inp_enc_level3 = self.down2_3(out_enc_level2)
        for encoder3_block in self.encoder_level3:
            inp_enc_level3, _, k = encoder3_block(inp_enc_level3, t_embed, k)
        out_enc_level3 = inp_enc_level3

        inp_enc_level4 = self.down3_4(out_enc_level3)       
        for latent_block in self.latent:
            inp_enc_level4, _, k = latent_block(inp_enc_level4, t_embed, k)
        latent = inp_enc_level4
                        
        inp_dec_level3 = self.up4_3(latent)
        inp_dec_level3 = torch.cat([inp_dec_level3, out_enc_level3], 1)
        inp_dec_level3 = self.reduce_chan_level3(inp_dec_level3)
        for decoder3_block in self.decoder_level3:
            inp_dec_level3, _, k  = decoder3_block(inp_dec_level3, t_embed, k)
        out_dec_level3 = inp_dec_level3

        inp_dec_level2 = self.up3_2(out_dec_level3)
        inp_dec_level2 = torch.cat([inp_dec_level2, out_enc_level2], 1)
        inp_dec_level2 = self.reduce_chan_level2(inp_dec_level2)
        for decoder2_block in self.decoder_level2:
            inp_dec_level2, _, k  = decoder2_block(inp_dec_level2, t_embed, k)
        out_dec_level2 = inp_dec_level2

        inp_dec_level1 = self.up2_1(out_dec_level2)
        inp_dec_level1 = torch.cat([inp_dec_level1, out_enc_level1], 1)
        for decoder1_block in self.decoder_level1:
            inp_dec_level1, _, k  = decoder1_block(inp_dec_level1, t_embed, k)
        out_dec_level1 = inp_dec_level1
        
        for refine_block in self.refinement:
            out_dec_level1, _, k = refine_block(out_dec_level1, t_embed, k)
            
        if (len(k.shape) ==1):
            k=torch.unsqueeze(k,0)
        k = self.k_mlp(k) 
        #### For Dual-Pixel Defocus Deblurring Task ####
        if self.dual_pixel_task:
            out_dec_level1 = out_dec_level1 + self.skip_conv(inp_enc_level1)
            out_dec_level1 = self.output(out_dec_level1)
        ###########################
        else:
            out_dec_level1 = self.output(out_dec_level1) + torch.cat([inp_img,inp_img],dim=1)

        return out_dec_level1, k